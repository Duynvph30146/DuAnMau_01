    private void LoadTable() {
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel = (DefaultTableModel) tblChucVu.getModel();
        tableModel.setColumnCount(0);
        tableModel.addColumn("ID");
        tableModel.addColumn("Mã");
        tableModel.addColumn("Tên");

        tableModel.setRowCount(0);
        ArrayList<ChucVuViewModel> listChucVu = chucVuSe.layDanhSachChucVu();
        for (ChucVuViewModel cv : listChucVu) {
            tableModel.addRow(new Object[]{cv.getID().toUpperCase(), cv.getMa().toUpperCase(),cv.getTen().toUpperCase()});
        }
    }

